// ================================
// Задание 2. Матрица и произведение
// элементов под чётными индексами
// ================================

const matrix = [
  [64, 362, 35, 786, 53],
  [73, 87, 0, 80, 367],
  [98, 457, 76, 21, 36],
  [64, 1, 765, 534, 26],
  [27, 852, 14, 98, 60]
];

let product = 1;

for (let i = 0; i < matrix.length; i++) {
  for (let j = 0; j < matrix[i].length; j++) {
    if (i % 2 === 0 && j % 2 === 0) {
      product *= matrix[i][j];
    }
  }
}

console.log('Произведение элементов под чётными индексами:');
console.log(product);
